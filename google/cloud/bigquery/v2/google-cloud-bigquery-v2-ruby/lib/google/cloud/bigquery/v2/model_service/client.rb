                  delete_model_config = parent_rpcs.delete_model if parent_rpcs.respond_to? :delete_model
                  @delete_model = ::Gapic::Config::Method.new delete_model_config

                  yield self if block_given?
                end
              end
            end
          end
        end
      end
    end
